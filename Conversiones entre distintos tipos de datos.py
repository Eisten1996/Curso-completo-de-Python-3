# de numero a cadena
numero = 5
cadena = str(numero)
print(type(cadena))

# de cadena a numero entero
cadena = "10"
numero2 = int(cadena)
print(type(numero2))
suma = numero + numero2
print(suma)

# de cadena a numero decimal
cadena = "10.6"
numero2 = float(cadena)
print(type(numero2))
suma = numero + numero2
print(suma)