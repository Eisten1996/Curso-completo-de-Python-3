# Operadores aritmetico (+, -, *, /, *, **, //)

numero1 = 110
numero2 = 5

suma = numero1 + numero2
print(suma)

resta = numero1 - numero2
print(resta)

multiplicacion = numero1 * numero2
print(multiplicacion)

division = numero1 / numero2 / 5
print(division)

resto = numero1 / numero2 % 5
print(resto)

cociente = numero1 // 3
print(cociente)

exponente = numero2**5
print(exponente)
